  filtered<T extends { name?: string; desc?: string; tag?: string; points?: string[]; title?: string; items?: string[] }>(
    list: T[]
  ): T[] {
    const q = (this.search || '').trim().toLowerCase();
    if (!q) return list;

    return list.filter((x: any) => {
      const blob = [
        x.name,
        x.desc,
        x.tag,
        ...(x.points || []),
        x.title,
        ...(x.items || []),
      ]
        .filter(Boolean)
        .join(' ')
        .toLowerCase();

      return blob.includes(q);
    });
  }
